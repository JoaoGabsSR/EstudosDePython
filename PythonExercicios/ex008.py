m = int(input('Digite uma metragem: '))
print('Esse valor em: '
      'Milímetros: {}\n'.format(m * 1000),
      'Centímetros: {}\n'.format(m * 100),
      'Decímetros: {}\n'.format(m * 10),
      'Metros: {}\n'.format(m),
      'Decametros: {}\n'.format(m * 0.1),
      'Hectometro: {}\n'.format(m * 0.01),
      'Kilometro: {}\n'.format(m * 0.001))
